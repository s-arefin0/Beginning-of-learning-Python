# Total marks greater than equal 80 then A+
# Total marks greater than equal 70 and less than 80 then A
# Total marks greater than equal 60 and less than 70 then A-
# Otherwise F

Total_marks = 75
if Total_marks >= 80:
    print("A+")
elif Total_marks >= 70 and Total_marks < 80:
    print("A")
elif Total_marks >= 60 and Total_marks < 70:
    print("A-")
else:
    print("F")
    
    
