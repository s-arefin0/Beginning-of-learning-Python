Fruits = {"Apple, Orange, Banana"}
print(Fruits)
print(type(Fruits))


# declare set with a constructor
Fruits = set(["Apple", "Orange", "Banana"])
print(Fruits)
print(type(Fruits))

# set doesn't allow duplicate items


# empty set declaration
empty_set = {}
print(type(empty_set))

# empty set declaration
empty_set = set()
print(type(empty_set))