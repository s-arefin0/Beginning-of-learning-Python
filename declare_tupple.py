# making tupples in different way

# with bracket 
Fruits = ("Apple", "Banana", "Grapes")
print(type(Fruits))
print(Fruits)

# without bracket
Fruits = "Apple", "Banana", "Grapes"
print(type(Fruits))
print(Fruits)

# adding numbers, duplicate and other things
Everything = ("Apple", "Banana", "Grapes", "Grapes", "Banana", 1, 2, 3 , ["s"], {'b'})
print(type(Everything))
print(Everything)

#without bracket 
Everything = "Apple", "Banana", "Grapes", "Grapes", "Banana", 1, 2, 3 , ["s"], {'b'}
print(type(Everything))
print(Everything)

# single item in tupple
Fruits = ("Apple",)
print(type(Fruits))
print(Fruits)

Fruits = "Apple",
print(type(Fruits))
print(Fruits)
